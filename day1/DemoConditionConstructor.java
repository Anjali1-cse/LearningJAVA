class DemoConditionConstructor{
        public static void main(String [] args){   
               int age;
               age=18;
               char nationality='I';
               if(age>=18 && nationality =='I'){
                   System.out.println("Eligible for voting");
               }
               else{
                   System.out.println("NOT Eligible for voting"); 
               }
        }
  
}